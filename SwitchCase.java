public class SwitchCase {
    public static void main(String args[]){
      int i = 1;
      switch(i)
      {
         case 1:
       System.out.println("Case1 ");
       break;
         case 2:
       System.out.println("Case2");
         case 3:
       System.out.println("Case3");
         default:
       System.out.println("Default");
       }
    }
 }